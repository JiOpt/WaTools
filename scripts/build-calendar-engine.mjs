import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, '..');
const CR1 = path.join(__dirname, '_calendar-cr1.js');
const CR2 = path.join(__dirname, '_calendar-cr2.js');
const OUT = path.join(ROOT, 'assets', 'js', 'calendar-engine.js');

function sliceCr1(src) {
  const lines = src.split(/\r?\n/);
  const keep = [];
  for (let i = 0; i < lines.length; i++) {
    const n = i + 1;
    if (n >= 609 && n <= 699) continue;
    if (n >= 707 && n <= 712) continue;
    if (n >= 779) break;
    keep.push(lines[i]);
  }
  return keep.join('\n')
    .replace(/^AnimalIdx=/m, 'var AnimalIdx=')
    .replace(/^AnimalIdx2=/m, 'var AnimalIdx2=');
}

function sliceCr2(src) {
  const lines = src.split(/\r?\n/);
  const keep = [];
  let depth = 0;
  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    const n = i + 1;
    if (n > 86) break;
    if (line.includes('function showtip2')) break;
    keep.push(line);
  }
  return keep.join('\n');
}

