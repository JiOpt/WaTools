const file = path.join(__dirname, 'data/toeic-l2-extra-b.json');
const data = JSON.parse(fs.readFileSync(file, 'utf8'));

function toTrad(zh) {
  let out = zh;
  const keys = Object.keys(s2t).sort((a, b) => b.length - a.length);
  for (const k of keys) out = out.split(k).join(s2t[k]);
  return out;
}

for (const row of data['legal-ip']) {
  row[2] = toTrad(row[2]);
}

fs.writeFileSync(file, JSON.stringify(data, null, 2) + '\n', 'utf8');
