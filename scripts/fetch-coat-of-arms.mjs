/**
 * Fetch coat-of-arms images (mainfacts.com) and build coat-of-arms-data.js
 * Run: node scripts/fetch-coat-of-arms.mjs
 * Options: --skip-images  (reuse existing images, rebuild JS only)
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { enrichAll, ISO2_OVERRIDES, normalizeIso3 } from './coat-of-arms-enrich.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, '..');
const FLAGS_DATA = path.join(ROOT, 'assets', 'js', 'world-flags-data.js');
const ISO_MAP = path.join(__dirname, 'iso3166-alpha.json');
const IMG_DIR = path.join(ROOT, 'coat-of-arms-img');
const OUT_DATA = path.join(ROOT, 'assets', 'js', 'coat-of-arms-data.js');

function loadFlagsData() {
  const src = fs.readFileSync(FLAGS_DATA, 'utf8');
  const sandbox = { window: {} };
  const wrapped = src.replace(/window\.WA_WORLD_FLAGS\s*=/, 'sandbox.window.WA_WORLD_FLAGS =');
  // eslint-disable-next-line no-new-func
  new Function('sandbox', wrapped)(sandbox);
  return sandbox.window.WA_WORLD_FLAGS;
}

function iso2ForCode(code, isoMap) {
  const key = (code || '').toLowerCase();
  if (ISO2_OVERRIDES[key]) return ISO2_OVERRIDES[key];
  const iso3 = normalizeIso3(key);
  return (isoMap[iso3] || '').toLowerCase();
}

async function downloadImage(url, dest) {
  if (fs.existsSync(dest)) return true;
  const res = await fetch(url, {
    headers: { 'User-Agent': 'MyTooLife/1.0 (coat-of-arms fetch)' },
  });
  if (!res.ok) return false;
  const buf = Buffer.from(await res.arrayBuffer());
  fs.writeFileSync(dest, buf);
  return true;
}

async function main() {
  const skipImages = process.argv.includes('--skip-images');
  const flagsData = loadFlagsData();
  const isoMap = JSON.parse(fs.readFileSync(ISO_MAP, 'utf8'));

  fs.mkdirSync(IMG_DIR, { recursive: true });

  const regions = flagsData.regions.map((r) => ({
    id: r.id,
    label: r.label,
    countries: r.countries.map((c) => ({ ...c })),
  }));

  let ok = 0;
  let fail = 0;

  if (!skipImages) {
    for (const region of regions) {
      for (const country of region.countries) {
        const iso2 = iso2ForCode(country.code, isoMap);
        if (!iso2) {
          country.image = null;
          fail += 1;
          continue;
        }
        const filename = `${country.code || iso2}_coat.png`;
        country.image = `coat-of-arms-img/${filename}`;
        const dest = path.join(ROOT, country.image);
        const url = `https://mainfacts.com/media/images/coats_of_arms/${iso2}.png`;
        try {
          const saved = await downloadImage(url, dest);
          if (saved) {
            ok += 1;
            process.stdout.write('.');
          } else {
            country.image = null;
            fail += 1;
            process.stdout.write('x');
          }
        } catch {
          country.image = null;
          fail += 1;
          process.stdout.write('x');
        }
      }
    }
    console.log(`\nImages: ${ok} ok, ${fail} missing.`);
  } else {
    for (const region of regions) {
      for (const country of region.countries) {
        const filename = `${country.code}_coat.png`;
        const rel = `coat-of-arms-img/${filename}`;
        country.image = fs.existsSync(path.join(ROOT, rel)) ? rel : null;
      }
    }
    console.log('Skipped image download (--skip-images).');
  }

  const enriched = enrichAll(regions);
  const total = enriched.reduce((n, r) => n + r.countries.length, 0);
  const withImg = enriched.reduce((n, r) => n + r.countries.filter((c) => c.image).length, 0);

  const payload = {
    source: 'MyTooLife / mainfacts.com coat of arms images',
    imageNote: 'Images via mainfacts.com; descriptions curated and enriched locally.',
    countryCount: total,
    imageCount: withImg,
    regions: enriched.map((r) => ({
      id: r.id,
      label: r.label,
      countries: r.countries.map((c) => ({
        nameEn: c.nameEn,
        nameZh: c.nameZh,
        code: c.code,
        image: c.image,
        coatName: c.coatName,
        coatType: c.coatType,
        symbols: c.symbols,
        history: c.history,
        composition: c.composition,
        desc: c.desc,
        natives: c.natives,
      })),
    })),
  };

  const out = `/* Auto-generated by scripts/fetch-coat-of-arms.mjs — do not edit */\nwindow.WA_COAT_OF_ARMS = ${JSON.stringify(payload, null, 2)};\n`;
  fs.writeFileSync(OUT_DATA, out, 'utf8');
  console.log(`Wrote ${total} countries (${withImg} with images) → ${path.relative(ROOT, OUT_DATA)}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
