/**
 * Download car brand logos and build car-brand-data.js
 * Run: node scripts/fetch-car-brand.mjs
 * Options: --skip-images  (reuse existing images, rebuild JS only)
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { CAR_BRAND_REGIONS, CAR_BRAND_INTRO } from './car-brand-enrich.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, '..');
const IMG_DIR = path.join(ROOT, 'life', 'car-brand-img');
const OUT_DATA = path.join(ROOT, 'assets', 'js', 'car-brand-data.js');

function wikiFileUrl(filename) {
  return `https://commons.wikimedia.org/wiki/Special:FilePath/${encodeURIComponent(filename)}`;
}

function simpleIconUrl(slug) {
  return `https://cdn.jsdelivr.net/npm/simple-icons@11.14.0/icons/${slug}.svg`;
}

function brandInitialSvg(name) {
  const letter = (name || '?').charAt(0).toUpperCase();
  return `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 120 80"><rect width="120" height="80" rx="8" fill="#f0f0f0"/><text x="60" y="48" text-anchor="middle" font-family="system-ui,sans-serif" font-size="28" font-weight="700" fill="#555">${letter}</text></svg>`;
}

function extFromContentType(ct, url) {
  if (ct?.includes('svg')) return '.svg';
  if (ct?.includes('png')) return '.png';
  if (ct?.includes('jpeg') || ct?.includes('jpg')) return '.jpg';
  const m = url.match(/\.(svg|png|jpe?g|webp)(\?|$)/i);
  return m ? `.${m[1].toLowerCase().replace('jpeg', 'jpg')}` : '.png';
}

function sleep(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

async function downloadLogo(filename, destBase) {
  const url = wikiFileUrl(filename);
  const res = await fetch(url, {
    headers: { 'User-Agent': 'MyTooLife/1.0 (car-brand fetch)' },
    redirect: 'follow',
  });
  if (!res.ok) {
    console.warn(`  FAIL ${filename}: HTTP ${res.status}`);
    return null;
  }
  const ct = res.headers.get('content-type') || '';
  const ext = extFromContentType(ct, res.url);
  const dest = `${destBase}${ext}`;
  const buf = Buffer.from(await res.arrayBuffer());
  if (buf.length < 200) {
    console.warn(`  FAIL ${filename}: file too small (${buf.length} bytes)`);
    return null;
  }
  fs.writeFileSync(dest, buf);
  return path.basename(dest);
}

async function downloadFromSimpleIcons(slug, destBase) {
  const url = simpleIconUrl(slug);
  const res = await fetch(url, { headers: { 'User-Agent': 'MyTooLife/1.0' } });
  if (!res.ok) return null;
  const dest = `${destBase}.svg`;
  fs.writeFileSync(dest, Buffer.from(await res.arrayBuffer()));
  return path.basename(dest);
}

async function downloadBrandLogo(brand, destBase) {
  if (brand.simpleIconSlug) {
    const saved = await downloadFromSimpleIcons(brand.simpleIconSlug, destBase);
    if (saved) return saved;
  }

  const candidates = [brand.logoFile, ...(brand.logoFallbacks || [])].filter(Boolean);
  for (let i = 0; i < candidates.length; i++) {
    if (i > 0) await sleep(600);
    const saved = await downloadLogo(candidates[i], destBase);
    if (saved) return saved;
  }

  const dest = `${destBase}.svg`;
  fs.writeFileSync(dest, brandInitialSvg(brand.nameEn || brand.nameZh), 'utf8');
  console.log(`  fallback initial: ${brand.id}`);
  return path.basename(dest);
}

function escJs(str) {
  return String(str)
    .replace(/\\/g, '\\\\')
    .replace(/'/g, "\\'")
    .replace(/\n/g, '\\n');
}

function buildDataJs(regions) {
  const lines = [
    '/* Auto-generated by scripts/fetch-car-brand.mjs — do not edit */',
    'window.WA_CAR_BRANDS = {',
    `  intro: {`,
    `    summary: '${escJs(CAR_BRAND_INTRO.summary)}',`,
    `    guide: [`,
    ...CAR_BRAND_INTRO.guide.map((g) => `      '${escJs(g)}',`),
    `    ],`,
    `    disclaimer: '${escJs(CAR_BRAND_INTRO.disclaimer)}',`,
    `  },`,
    '  regions: [',
  ];

  for (const region of regions) {
    lines.push(`    {`);
    lines.push(`      id: '${region.id}',`);
    lines.push(`      label: '${escJs(region.label)}',`);
    lines.push(`      brands: [`);
    for (const brand of region.brands) {
      lines.push(`        {`);
      lines.push(`          id: '${brand.id}',`);
      lines.push(`          nameZh: '${escJs(brand.nameZh)}',`);
      lines.push(`          nameEn: '${escJs(brand.nameEn)}',`);
      lines.push(`          tier: '${escJs(brand.tier)}',`);
      if (brand.group) lines.push(`          group: '${escJs(brand.group)}',`);
      lines.push(`          image: 'life/car-brand-img/${brand.imageFile}',`);
      lines.push(`          desc: '${escJs(brand.desc)}',`);
      lines.push(`        },`);
    }
    lines.push(`      ],`);
    lines.push(`    },`);
  }

  lines.push('  ],');
  lines.push('};');
  lines.push('');
  return lines.join('\n');
}

async function main() {
  const skipImages = process.argv.includes('--skip-images');
  fs.mkdirSync(IMG_DIR, { recursive: true });

  const regions = [];

  for (const region of CAR_BRAND_REGIONS) {
    const brands = [];
    for (const brand of region.brands) {
      const destBase = path.join(IMG_DIR, brand.id);
      let imageFile = `${brand.id}.svg`;

      if (!skipImages) {
        const existing = ['.svg', '.png', '.jpg', '.webp']
          .map((ext) => `${destBase}${ext}`)
          .find((p) => fs.existsSync(p));
        if (existing) {
          imageFile = path.basename(existing);
        } else {
          console.log(`  fetch ${brand.id}`);
          await sleep(150);
          const saved = await downloadBrandLogo(brand, destBase);
          imageFile = saved || 'placeholder.svg';
        }
      } else {
        const existing = ['.svg', '.png', '.jpg', '.webp']
          .map((ext) => `${destBase}${ext}`)
          .find((p) => fs.existsSync(p));
        imageFile = existing ? path.basename(existing) : `${brand.id}.svg`;
      }

      brands.push({ ...brand, imageFile });
    }
    regions.push({ ...region, brands });
  }

  if (!fs.existsSync(path.join(IMG_DIR, 'placeholder.svg'))) {
    fs.writeFileSync(path.join(IMG_DIR, 'placeholder.svg'), `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 120 80"><rect width="120" height="80" fill="#e8e8e8"/><text x="60" y="44" text-anchor="middle" font-family="sans-serif" font-size="12" fill="#888">Logo</text></svg>`);
  }

  fs.writeFileSync(OUT_DATA, buildDataJs(regions), 'utf8');
  const total = regions.reduce((n, r) => n + r.brands.length, 0);
  console.log(`\nWrote ${OUT_DATA} (${total} brands)`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
