/** @param {Array<[string,string,string,string]>} pool */
function dedupe(pool) {
  const seen = new Set();
  return pool.filter(([w]) => {
    const k = w.trim().toLowerCase();
    if (!k || seen.has(k)) return false;
    seen.add(k);
    return true;
  });
}

/** @param {Array<[string,string,string,string]>} pool @param {number} n */
function pick(pool, n) {
  const u = dedupe(pool);
  if (u.length < n) throw new Error(`Need ${n}, got ${u.length}`);
  return u.slice(0, n);
}

