/** @param {string} name @param {string} exportName @param {Array<[string,string,string,string]>} data */
function writePool(name, exportName, data) {
  const p = path.join(__dirname, `toefl-l2-pool-${name}.mjs`);
  fs.writeFileSync(p, `/** TOEFL L2 ${name} pool (${data.length} entries) */\nexport const ${exportName} = ${JSON.stringify(data, null, 2)};\n`, 'utf8');
  console.log(`${name}: ${data.length}`);
}

