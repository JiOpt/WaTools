import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));

function writePool(name, exportName, data) {
  const seen = new Set();
  const unique = data.filter(([w]) => {
    const k = w.toLowerCase();
    if (seen.has(k)) return false;
    seen.add(k);
    return true;
  });
  const p = path.join(__dirname, `toefl-l2-pool-${name}.mjs`);
  fs.writeFileSync(p, `/** TOEFL L2 ${name} pool (${unique.length} entries) */\nexport const ${exportName} = ${JSON.stringify(unique, null, 2)};\n`, 'utf8');
  console.log(`${name}: ${unique.length}`);
  return unique.length;
}

