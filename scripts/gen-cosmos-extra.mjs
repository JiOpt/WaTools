/**
 * Generate assets/js/cosmos-theme.js from scripts/data/cosmos-themes.mjs
 * Run: node scripts/gen-cosmos-extra.mjs
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { COSMOS_THEMES } from './data/cosmos-themes.mjs';

const ROOT = path.join(path.dirname(fileURLToPath(import.meta.url)), '..');
const imgMapPath = path.join(ROOT, 'scripts', 'data', 'cosmos-img-map.json');
const imgMap = fs.existsSync(imgMapPath)
  ? JSON.parse(fs.readFileSync(imgMapPath, 'utf8'))
  : { files: [], byTheme: {} };

function u(id) {
  return `https://images.unsplash.com/${id}?auto=format&fit=crop&w=900&q=75`;
}

function sectionImg(themeId, index, fallbackId) {
  const local = imgMap.byTheme?.[themeId]?.[index];
  if (local) return `./img/${local}`;
  const pool = imgMap.files || [];
  if (pool.length) return `./img/${pool[index % pool.length]}`;
  return u(fallbackId);
}

const themes = COSMOS_THEMES.map((t) => ({
  id: t.id,
  title: t.title,
  eyebrow: t.eyebrow,
  metaphor: t.metaphor,
  lead: t.lead,
  whyHot: t.whyHot,
  sections: (t.sections || []).map((s, i) => ({
    tag: s.tag,
    title: s.title,
    body: s.body,
    img: sectionImg(t.id, i, s.img),
    alt: s.alt || s.title,
  })),
  glossary: t.glossary || [],
  takeaway: String(t.takeaway || '').replace(/\bawe\b/gi, '敬畏'),
}));

const outJs = `/**
 * Cosmic Atlas — illustrated universe themes.
 * Auto-generated by scripts/gen-cosmos-extra.mjs — edit scripts/data/cosmos-themes.mjs
 */
(function (global) {
  'use strict';

  var THEMES = ${JSON.stringify(themes, null, 2)};

  function assetUrl(rel) {
    try { return new URL(rel, global.location.href).href; } catch (e) { return rel; }
  }

  function ensureCss(href) {
    var key = 'wa-cx-css:' + href;
    if (document.querySelector('link[data-wa-key="' + key + '"]')) return;
    var link = document.createElement('link');
    link.rel = 'stylesheet';
    link.href = assetUrl(href);
    link.setAttribute('data-wa-key', key);
    document.head.appendChild(link);
  }

  function loadCss() {
    ensureCss('./css/shared.css?v=cx-3');
  }

  function cleanupFns(app) {
    (app.__waCxCleanup || []).forEach(function (fn) { try { fn(); } catch (e) {} });
    app.__waCxCleanup = [];
  }

  function onCleanup(app, fn) {
    if (!app.__waCxCleanup) app.__waCxCleanup = [];
    app.__waCxCleanup.push(fn);
  }

  function esc(s) {
    return String(s == null ? '' : s)
      .replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;').replace(/"/g, '&quot;');
  }

  function mountTheme(cfg) {
    return function (app) {
      loadCss();
      cleanupFns(app);
      app.classList.add('wa-cosmos');

      var state = { filter: 'all' };
      var tags = ['all'].concat(
        cfg.sections.map(function (s) { return s.tag; }).filter(function (t, i, a) { return a.indexOf(t) === i; })
      );
      var tagLabel = { all: '全部章節', '比喻': '生活比喻', '核心': '核心概念', '關鍵': '關鍵名詞', '啟示': '啟發延伸' };

      app.innerHTML =
        '<article class="cx-shell">' +
        '  <p class="cx-eyebrow">浩瀚宇宙 Cosmic Atlas · ' + esc(cfg.eyebrow) + '</p>' +
        '  <h2 class="cx-title">' + esc(cfg.title) + '</h2>' +
        '  <p class="cx-metaphor">' + esc(cfg.metaphor) + '</p>' +
        '  <p class="cx-lead">' + esc(cfg.lead) + '</p>' +
        '  <p class="cx-why"><strong>為什麼熱門：</strong>' + esc(cfg.whyHot) + '</p>' +
        '  <div class="cx-chips" id="cx-chips" role="tablist" aria-label="章節篩選"></div>' +
        '  <div class="cx-grid" id="cx-grid"></div>' +
        '  <section class="cx-glossary">' +
        '    <h3>關鍵詞速查</h3>' +
        '    <dl>' + (cfg.glossary || []).map(function (g) {
          return '<div><dt>' + esc(g.term) + '</dt><dd>' + esc(g.def) + '</dd></div>';
        }).join('') + '</dl>' +
        '  </section>' +
        '  <section class="cx-takeaway">' +
        '    <h3>帶著走的啟發</h3>' +
        '    <p>' + esc(cfg.takeaway) + '</p>' +
        '  </section>' +
        '  <p class="cx-note">內容為科普整理，觀測與任務進度可能更新；歡迎收藏後再次閱讀，把宇宙的好奇心傳下去。</p>' +
        '</article>';

      var chips = app.querySelector('#cx-chips');
      var grid = app.querySelector('#cx-grid');

      function renderChips() {
        chips.innerHTML = tags.map(function (t) {
          var on = state.filter === t ? ' is-on' : '';
          return '<button type="button" class="cx-chip' + on + '" data-tag="' + esc(t) + '" aria-pressed="' +
            (state.filter === t ? 'true' : 'false') + '">' + esc(tagLabel[t] || t) + '</button>';
        }).join('');
      }

      function renderGrid() {
        var list = cfg.sections.filter(function (s) {
          return state.filter === 'all' || s.tag === state.filter;
        });
        grid.innerHTML = list.map(function (s) {
          return (
            '<section class="cx-card">' +
            '  <div class="cx-card__media"><img src="' + esc(s.img) + '" alt="' + esc(s.alt) + '" loading="lazy" width="900" height="560" /></div>' +
            '  <div class="cx-card__body">' +
            '    <span class="cx-card__tag">' + esc(tagLabel[s.tag] || s.tag) + '</span>' +
            '    <h3 class="cx-card__title">' + esc(s.title) + '</h3>' +
            '    <p class="cx-card__text">' + esc(s.body) + '</p>' +
            '  </div>' +
            '</section>'
          );
        }).join('');
        grid.querySelectorAll('img').forEach(function (img) {
          function done() { img.classList.add('is-loaded'); }
          if (img.complete) {
            done();
            return;
          }
          img.addEventListener('load', done, { once: true });
          img.addEventListener('error', function () {
            img.removeAttribute('src');
            img.alt = (img.alt || 'Cosmos') + '（圖檔載入失敗）';
            img.style.background = 'linear-gradient(135deg,#0f172a,#1e3a5f)';
            done();
          }, { once: true });
        });
      }

      function onChip(e) {
        var b = e.target.closest('.cx-chip');
        if (!b || !chips.contains(b)) return;
        state.filter = b.getAttribute('data-tag') || 'all';
        renderChips();
        renderGrid();
      }

      chips.addEventListener('click', onChip);
      onCleanup(app, function () { chips.removeEventListener('click', onChip); });
      onCleanup(app, function () { app.classList.remove('wa-cosmos'); });

      renderChips();
      renderGrid();
    };
  }

  var mounts = {};
  THEMES.forEach(function (cfg) {
    mounts[cfg.id] = mountTheme(cfg);
  });
  global.WA_MOUNT_COSMOS = Object.assign(global.WA_MOUNT_COSMOS || {}, mounts);
})(typeof window !== 'undefined' ? window : this);
`;

fs.writeFileSync(path.join(ROOT, 'assets/js/cosmos-theme.js'), outJs, 'utf8');
console.log('Wrote cosmos-theme.js with', themes.length, 'themes');
