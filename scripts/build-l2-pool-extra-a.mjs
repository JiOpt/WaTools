import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const L2_FILE = path.join(__dirname, '../assets/js/toeic-vocab-l2-data.js');
const OUT_DIR = path.join(__dirname, 'data');

function parseExistingWords(themeId) {
  const src = fs.readFileSync(L2_FILE, 'utf8');
  const marker = `id: '${themeId}'`;
  const start = src.indexOf(marker);
  if (start < 0) throw new Error(`Theme not found: ${themeId}`);
  const wordsStart = src.indexOf('words: [', start);
  const wordsEnd = src.indexOf('\n      ]', wordsStart);
  const block = src.slice(wordsStart, wordsEnd);
  return new Set([...block.matchAll(/w: '([^']+)'/g)].map((m) => m[1].toLowerCase()));
}

function e(word, pos, zh, syn) {
  return [word, pos, zh, syn];
}

function dedupePool(pool, existing) {
  const seen = new Set(existing);
  const out = [];
  for (const row of pool) {
    const key = row[0].toLowerCase();
    if (seen.has(key)) continue;
    seen.add(key);
    out.push(row);
  }
  return out;
}

function writePool(filename, exportName, pool, minCount) {
  const deduped = dedupePool(pool, parseExistingWords(
    exportName === 'supplyChain' ? 'supply-chain'
      : exportName === 'hrLabor' ? 'hr-labor'
        : exportName === 'marketingPr' ? 'marketing-pr'
          : 'auditing',
  ));
  if (deduped.length < minCount) {
    throw new Error(`${filename}: only ${deduped.length} unique entries (need ${minCount}+)`);
  }
  const lines = deduped.map((r) => `  ${JSON.stringify(r)},`).join('\n');
  const content = `/* eslint-disable */\n/** TOEIC L2 extra-a vocabulary pool — ${exportName} */\nconst ${exportName} = [\n${lines}\n];\n\nmodule.exports = { ${exportName} };\n`;
  fs.writeFileSync(path.join(OUT_DIR, filename), content, 'utf8');
  console.log(`${filename}: ${deduped.length} unique (min ${minCount})`);
  return deduped.length;
}

